INSERT INTO S(A) VALUES ('a1'), ('a1'), ('a2'), ('a3'), ('a4'), ('a5');

SELECT * FROM R;
SELECT * FROM S;

-- Usando EXCEPT utilizando NOT IN
SELECT * FROM S WHERE A NOT IN (SELECT A FROM R);

